int		ft_placeblock6(char **map, int *v, int code, char c)
{
	if (code == 9 && (v[0] <= v[2] - 1) && (v[0] > 1) && (v[1] <= v[2] - 2))
	{
		if (map[v[1]][v[0]] == '.' && map[v[1] + 1][v[0] - 2] == '.' &&
		map[v[1] + 1][v[0] - 1] == '.' && map[v[1] + 1][v[0]] == '.')
		{
			map[v[1]][v[0]] = c;
			map[v[1] + 1][v[0] - 2] = c;
			map[v[1] + 1][v[0] - 1] = c;
			map[v[1] + 1][v[0]] = c;
			return (1);
		}
	}
	if (code == 10 && (v[0] <= v[2] - 1) && (v[0] > 0) && (v[1] <= v[2] - 3))
	{
		if (map[v[1]][v[0]] == '.' && map[v[1] + 1][v[0]] == '.' &&
		map[v[1] + 2][v[0]] == '.' && map[v[1] + 2][v[0] - 1] == '.')
		{
			map[v[1]][v[0]] = c;
			map[v[1] + 1][v[0]] = c;
			map[v[1] + 2][v[0]] = c;
			map[v[1] + 2][v[0] - 1] = c;
			return (1);
		}
	}
	return (0);
}

